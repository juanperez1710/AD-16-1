/**1. Modify the style of the paragraph text through JavaScript code.
By clicking on the button, the font, font size, and colour of the paragraph text should change. */
function js_style(){
    var textoParrafo=document.getElementById("text");
    textoParrafo.style.color="blue";
    textoParrafo.style.fontSize="30pt";
    textoParrafo.style.fontFamily="Arial,sas-serif";
}